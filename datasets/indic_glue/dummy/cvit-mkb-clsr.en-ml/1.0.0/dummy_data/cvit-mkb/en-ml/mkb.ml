ഇന്ന് ദീപാവലിയുടെ പുണ്യദിനമാണ്.
ഇക്കാലത്ത് ലോകത്തെ പല രാജ്യങ്ങളിലും ദീപാവലി ആഘോഷിക്കപ്പെടുന്നുണ്ട്.
ഭാരതീയ സമൂഹം മാത്രമല്ല മറിച്ച് പല രാജ്യങ്ങളിലെ ഗവണ്‍മെന്റുകളും അവിടത്തെ പൗരന്മാരും അവിടത്തെ സാമൂഹിക സംഘടനകളും ദീപാവലി തികഞ്ഞ സന്തോഷത്തോടും ഉല്ലാസത്തോടും കൂടി ആഘോഷിക്കുന്നു എന്നതാണ് ഈ ആഘോഷത്തിന്റെ വൈശിഷ്ട്യം.
ഇങ്ങനെ അവിടെ ഭാരതത്തിന്റെ ഒരു സൂക്ഷ്മ ലോകം കാട്ടിത്തരുകയാണ് ചെയ്യുന്നത്.
സുഹൃത്തുക്കളേ, ലോകത്ത് ആഘോഷവുമായി ബന്ധപ്പെട്ട വിനോദസഞ്ചാരം ഫെസ്റ്റിവല്‍ ടൂറിസത്തിന് അതിന്റെതായ ആകര്‍ഷണമുണ്ട്.